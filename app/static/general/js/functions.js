function message_error(obj) {
    contentStr = "";
    $.each(obj, function (key, value) {
        error = value;
        contentStr += "<div class='alert alert-danger alert-dismissible msg_alert' id='msg_alert'>" + "<button type='button' class='close' data-dismiss='alert' aria-hidden='true'>" + "Ã—" + "</button>" + "<h5>" + "<i class='icon fas fa-ban'>" + "</i>" + "Ha ocurrido un error..." + "</h5>" + "<ul>" + error + "</ul>" + "</div>";
    });
    document.getElementById('div_alert').innerHTML = contentStr;
}

function submit_with_ajax(url, title, content, parameters, callback) {
    $.confirm({
        theme: 'material',
        title: title,
        icon: 'fa fa-info',
        content: content,
        columnClass: 'small',
        typeAnimated: true,
        cancelButtonClass: 'btn-primary',
        draggable: true,
        dragWindowBorder: false,
        buttons: {
            info: {
                text: "Si",
                btnClass: 'btn-primary',
                action: function () {
                    $.ajax({
                        url: url, //window.location.pathname
                        type: 'POST',
                        data: parameters,
                        dataType: 'json',
                        processData: false,
                        contentType: false,
                    }).done(function (data) {
                        if (!data.hasOwnProperty('error')) {
                            callback();
                            return false;
                        }
                        message_error(data.error);
                    }).fail(function (jqXHR, textStatus, errorThrown) {
                        alert(textStatus + ': ' + errorThrown);
                    }).always(function (data) {

                    });
                }
            },
            danger: {
                text: "No",
                btnClass: 'btn-red',
                action: function () {

                }
            },
        }
    })
}

